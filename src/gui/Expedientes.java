    int addExpediente() throws SQLException {
        Paciente paciente = (Paciente) jComboBox1.getSelectedItem();
        int pac = paciente.getIdpaciente();
        String ch = jTextField1.getText();
        String pes = jTextField2.getText();
        String alt = jTextField4.getText();
        String vac = jTextField5.getText();
        String med = jTextField6.getText();
        String f_c = jTextField7.getText();
        String f_r = jTextField8.getText();
        String pul = jTextField9.getText();
        String obs = jTextArea1.getText();
        
        Expediente expediente = new Expediente(pac, ch, pes, alt, vac, med, f_r, f_r, pul, obs);
        int id = expedienteDao.insertar(expediente);
        jTable1.setModel(pacienteDao.cargarModelo());
        loadCombooC();
        return id;

    }
